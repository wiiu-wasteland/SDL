/* GX2 shader implementation */

struct GX2_ShaderContext
{
    WHBGfxShaderGroup shaders[NUM_SHADERS];
    GX2_Shader currentShader;
};

unsigned char _gx2_color_shader[];
unsigned char _gx2_texture_shader[];

GX2_ShaderContext *
GX2_CreateShaderContext(void)
{
    WHBGfxShaderGroup *shader;
    GX2_ShaderContext *ctx;

    ctx = (GX2_ShaderContext *)SDL_calloc(1, sizeof(*ctx));
    if (!ctx) {
        return NULL;
    }

    shader = &ctx->shaders[SHADER_TEXTURE];
    WHBGfxLoadGFDShaderGroup(shader, 0, _gx2_texture_shader);
    WHBGfxInitShaderAttribute(shader, "a_position", 0, 0, GX2_ATTRIB_FORMAT_FLOAT_32_32);
    WHBGfxInitShaderAttribute(shader, "a_texCoordIn", 1, 0, GX2_ATTRIB_FORMAT_FLOAT_32_32);
    WHBGfxInitFetchShader(shader);

    shader = &ctx->shaders[SHADER_COLOR];
    WHBGfxLoadGFDShaderGroup(shader, 0, _gx2_color_shader);
    WHBGfxInitShaderAttribute(shader, "a_position", 0, 0, GX2_ATTRIB_FORMAT_FLOAT_32_32);
    WHBGfxInitFetchShader(shader);

    return ctx;
}

void
GX2_SelectShader(GX2_ShaderContext *ctx, GX2_Shader shader)
{
    GX2SetFetchShader(&ctx->shaders[shader].fetchShader);
    GX2SetVertexShader(ctx->shaders[shader].vertexShader);
    GX2SetPixelShader(ctx->shaders[shader].pixelShader);
}

void
GX2_SetVertexShaderUniform(GX2_ShaderContext *ctx, uint32_t index, uint32_t count, void *data)
{
    WHBGfxShaderGroup *cur = &ctx->shaders[ctx->currentShader];
    GX2SetVertexUniformReg(cur->vertexShader->uniformVars[index].offset, count, data);
}

void
GX2_SetFragmentShaderUniform(GX2_ShaderContext *ctx, uint32_t index, uint32_t count, void *data)
{
    WHBGfxShaderGroup *cur = &ctx->shaders[ctx->currentShader];
    GX2SetPixelUniformReg(cur->pixelShader->uniformVars[index].offset, count, data);
}

void
GX2_DestroyShaderContext(GX2_ShaderContext *ctx)
{
    int i;
    
    for (i = 0; i < NUM_SHADERS; ++i) {
        WHBGfxFreeShaderGroup(&ctx->shaders[i]);
    }
    SDL_free(ctx);
}

